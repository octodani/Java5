    private void buttonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInsertarActionPerformed
        Producto p = new Producto();
        p.setCodigo(this.textCodigo.getText());
        p.setNombre(this.textNombre.getText());
        p.setCantidad(this.textCantidad.getText());
        p.setDescripcion(this.textDescripcion.getText());
        p.registrar();
        this.textCodigo.setText("");
        this.textNombre.setText("");
        this.textCantidad.setText("");
        this.textDescripcion.setText("");
        this.areaText.setBackground(fondo.VERDE);
        this.areaText.setText("OK. Entrada introducida");
    }//GEN-LAST:event_buttonInsertarActionPerformed

    private void botonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarActionPerformed
        Producto p = new Producto();
        this.areaText.setBackground(fondo.BLANCO);
        this.areaText.setText(p.mostrar());
    }//GEN-LAST:event_botonMostrarActionPerformed

    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        Producto p = new Producto();
        this.areaText.setBackground(fondo.BLANCO);
        this.areaText.setText(p.buscar(this.textCodigo.getText()));
        this.areaText.setBackground(fondo.getColor());
    }//GEN-LAST:event_botonBuscarActionPerformed

